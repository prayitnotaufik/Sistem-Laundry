    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        if (txtId.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "ID pelanggan harus diisi !");
        } else if (txtNama.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Nama harus diisi !");
        } else if (txtAlamat.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Alamat harus diisi !");
        } else if (txtTelp.getText() == null) {
            JOptionPane.showMessageDialog(null, "Nomor Telepon harus diisi !");
        } else {
            buka_koneksi();
            String sqlkode = "INSERT INTO pelanggan (id_pelanggan, nama_pelanggan, no_telp, alamat) "
                    + "VALUES ('"
                    + this.txtId.getText() + "','"
                    + this.txtNama.getText() + "','"
                    + this.txtTelp.getText() + "','"
                    + this.txtAlamat.getText() + "')";
            try {
                PreparedStatement mStatement = koneksi.prepareStatement(sqlkode);
                mStatement.executeUpdate();
                mStatement.close();
                JOptionPane.showMessageDialog(this, "Data berhasil ditambah");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Terjadi Kesalahan " + ex.getMessage());
            }
        }
        ambil_data_tabel();
    }//GEN-LAST:event_btnTambahActionPerformed

